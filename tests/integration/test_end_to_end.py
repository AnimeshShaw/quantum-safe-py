class _MockKEMBackend(AbstractKEMBackend):
    name = "mock"
    def keygen(self, a): return b"\xAA" * 1184, b"\xBB" * 2400
    def encapsulate(self, a, p): return b"\xCC" * 1088, b"\xDD" * 32
    def decapsulate(self, a, s, c): return b"\xDD" * 32
    def is_available(self): return True
    def supported_algorithms(self): return []


class _MockSigBackend(AbstractSignatureBackend):
    name = "mock"
    def keygen(self, a): return b"\xAA" * 1952, b"\xBB" * 4000
    def sign(self, a, sk, msg, ctx=b""): return b"\xCC" * 3293
    def verify(self, a, pk, msg, sig, ctx=b""): return len(sig) == 3293
    def is_available(self): return True
    def supported_algorithms(self): return []


def _mock_kem():
    k = HybridKEM.__new__(HybridKEM)
    k._classical = "X25519"
    k._pqc = "ML-KEM-768"
    k._algorithm = "X25519+ML-KEM-768"
    k._backend = _MockKEMBackend()
    return k


def _mock_sign():
    hs = HybridSign.__new__(HybridSign)
    hs._classical = "Ed25519"
    hs._pqc = "ML-DSA-65"
    hs._algorithm = "Ed25519+ML-DSA-65"
    hs._hedged = True
    hs._backend = _MockSigBackend()
    return hs


# ---------------------------------------------------------------------------
# Workflow 1: key generation → serialization → round-trip
# ---------------------------------------------------------------------------


class TestKeyLifecycle:
    """A key generated in one place must be usable in another."""

    def test_kem_keypair_pem_survives_serialization(self):
        kem = _mock_kem()
        kp = kem.generate_keypair()

        # Serialize public key
        pub_pem = kp.public.to_pem()
        pub_cbor = kp.public.to_cbor()
        pub_jwk = kp.public.to_jwk()

        # Deserialize all three formats
        pub_from_pem = PublicKey.from_pem(pub_pem)
        pub_from_cbor = PublicKey.from_cbor(pub_cbor)
        pub_from_jwk = PublicKey.from_jwk(pub_jwk)

        # All deserialized keys should be equal
        assert pub_from_pem.raw_bytes == kp.public.raw_bytes
        assert pub_from_cbor.raw_bytes == kp.public.raw_bytes
        assert pub_from_jwk.raw_bytes == kp.public.raw_bytes

    def test_signing_keypair_round_trip(self):
        signer = _mock_sign()
        kp = signer.generate_keypair()

        pub_pem = kp.public.to_pem()
        pub2 = PublicKey.from_pem(pub_pem)
        assert pub2.algorithm == kp.public.algorithm
        assert pub2.raw_bytes == kp.public.raw_bytes

    def test_key_fingerprints_stable(self):
        kem = _mock_kem()
        kp = kem.generate_keypair()
        fp1 = kp.public.fingerprint()
        fp2 = kp.public.fingerprint()
        assert fp1 == fp2
        assert len(fp1) == 64  # sha256 hex

    def test_keypair_bundle_round_trip(self):
        kem = _mock_kem()
        kp = kem.generate_keypair()
        bundle = kp.to_cbor_bundle()
        kp2 = KeyPair.from_cbor_bundle(bundle)
        assert kp2.public.raw_bytes == kp.public.raw_bytes
        assert kp2.secret.raw_bytes == kp.secret.raw_bytes


