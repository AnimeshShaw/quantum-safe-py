class TestMigrationWorkflow:
    """Key upgrade and state tracking must compose end-to-end."""

    def test_classical_key_to_hybrid_and_track(self):
        # Simulate a classical X25519 key being upgraded
        classical_pub = os.urandom(32)
        classical_sec = os.urandom(32)

        import quantum_safe.backends as _bk
        orig = _bk.get_kem_backend
        _bk.get_kem_backend = lambda name="auto": _MockKEMBackend()

        try:
            result = Upgrader.upgrade_kem_key(
                classical_secret_bytes=classical_sec,
                classical_public_bytes=classical_pub,
                classical_algorithm="X25519",
                target_pqc="ML-KEM-768",
            )
        finally:
            _bk.get_kem_backend = orig

        assert result.new_algorithm == "X25519+ML-KEM-768"
        assert result.backward_compat
        new_kp = result.new_keypair

        # Track in state machine
        store = {}
        mgr = MigrationStateManager(store)
        key_id = new_kp.public.fingerprint()[:16]

        rec = mgr.transition(
            key_id=key_id,
            from_state=MigrationState.CLASSICAL_ONLY,
            to_state=MigrationState.HYBRID_TRANSITION,
            algorithm=new_kp.algorithm,
            actor="integration-test",
        )

        assert mgr.get_current_state(key_id) == MigrationState.HYBRID_TRANSITION
        history = mgr.get_history(key_id)
        assert len(history) == 1
        assert history[0].actor == "integration-test"

    def test_upgrader_describe_key(self):
        classical_pk = PublicKey(
            raw=b"\x01" * 32,
            algorithm="X25519",
            migration_state=MigrationState.CLASSICAL_ONLY,
        )
        classical_sk = SecretKey(
            raw=b"\x02" * 32,
            algorithm="X25519",
            migration_state=MigrationState.CLASSICAL_ONLY,
        )
        kp = KeyPair(public=classical_pk, secret=classical_sk)
        desc = Upgrader.describe_key(kp)
        assert desc["needs_upgrade"] is True
        assert desc["migration_state"] == "classical_only"
        assert "Upgrade" in desc["recommendation"]


