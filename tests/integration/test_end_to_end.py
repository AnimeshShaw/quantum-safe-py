"""
End-to-end integration tests.

These tests exercise complete workflows from key generation through
encryption / signing / verification / migration without any mocking.
They use only the classical components (X25519, Ed25519) so they run
without liboqs installed. Tests that exercise actual ML-KEM / ML-DSA
are marked @pytest.mark.requires_liboqs.

The goal here is to prove the modules compose correctly — that a key
generated by HybridKEM can actually seal an Envelope, that a JWT signed
by JWTSigner can be verified by JWTVerifier, that the scanner findings
feed correctly into the compliance report.
"""

