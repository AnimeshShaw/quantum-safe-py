class TestX509Workflow:
    def test_cert_generation_and_inspection(self):
        from quantum_safe.protocols.x509 import (
            HybridCertificateBuilder, generate_classical_keypair_for_cert
        )
        from cryptography import x509 as cx509
        from cryptography.hazmat.backends import default_backend

        classical_key = generate_classical_keypair_for_cert("Ed25519")
        signer = _mock_sign()
        hybrid_kp = signer.generate_keypair()

        builder = HybridCertificateBuilder(
            subject_cn="integration.test.internal",
            classical_private_key=classical_key,
            pqc_keypair=hybrid_kp,
            validity_days=90,
            dns_names=["integration.test.internal", "alt.test.internal"],
        )
        cert_pem, cosig = builder.build(signer=signer)

        assert b"BEGIN CERTIFICATE" in cert_pem
        assert len(cosig) > 0

        cert = cx509.load_pem_x509_certificate(cert_pem, default_backend())
        cn = cert.subject.get_attributes_for_oid(
            cx509.oid.NameOID.COMMON_NAME
        )[0].value
        assert cn == "integration.test.internal"


