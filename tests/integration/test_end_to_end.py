class TestEnvelopeWorkflow:
    """Envelope must survive network serialization."""

    def test_seal_serialize_open(self):
        kem = _mock_kem()
        kp = kem.generate_keypair()

        # Sender: seal
        plaintext = b"confidential payload"
        sealed = Envelope.seal(plaintext, kp.public, kem=kem)

        # Wire: serialize to bytes
        wire = sealed.to_bytes()
        assert len(wire) > 0

        # Receiver: deserialize and open
        sealed2 = SealedMessage.from_bytes(wire)
        recovered = Envelope.open(sealed2, kp.secret, kem=kem)

        assert recovered == plaintext

    def test_seal_hex_round_trip(self):
        kem = _mock_kem()
        kp = kem.generate_keypair()
        sealed = Envelope.seal(b"hex payload", kp.public, kem=kem)
        hex_str = sealed.to_hex()
        assert isinstance(hex_str, str)
        sealed2 = SealedMessage.from_hex(hex_str)
        recovered = Envelope.open(sealed2, kp.secret, kem=kem)
        assert recovered == b"hex payload"

    def test_envelope_with_aad(self):
        kem = _mock_kem()
        kp = kem.generate_keypair()
        sealed = Envelope.seal(
            b"protected payload",
            kp.public,
            aad=b"recipient-id:user-42",
            kem=kem,
        )
        assert sealed.aad == b"recipient-id:user-42"
        recovered = Envelope.open(sealed, kp.secret, kem=kem)
        assert recovered == b"protected payload"

    def test_envelope_large_payload(self):
        kem = _mock_kem()
        kp = kem.generate_keypair()
        large = os.urandom(1024 * 64)  # 64 KB
        sealed = Envelope.seal(large, kp.public, kem=kem)
        recovered = Envelope.open(sealed, kp.secret, kem=kem)
        assert recovered == large

    def test_envelope_inspect_no_secrets(self):
        kem = _mock_kem()
        kp = kem.generate_keypair()
        sealed = Envelope.seal(b"secret", kp.public, kem=kem)
        info = sealed.inspect()
        assert "algorithm" in info
        assert "secret" not in str(info).lower()
        assert info["kem_ct_size"] > 0


