class TestAuditPipeline:
    """Scanning findings must flow correctly through audit and compliance."""

    _RSA_SOURCE = (
        "from cryptography.hazmat.primitives.asymmetric import rsa\n"
        "key = rsa.generate_private_key(65537, 2048)\n"
        "from cryptography.hazmat.primitives.asymmetric import padding\n"
        "enc = padding.OAEP(mgf=None, algorithm=None, label=None)\n"
    )

    def test_scanner_to_auditor_to_compliance(self):
        # Step 1: scan
        scan = Scanner.scan_source(self._RSA_SOURCE, filename="app.py")
        assert scan.has_blocking_findings

        # Step 2: audit
        report = Auditor.audit_source(self._RSA_SOURCE, policy=AuditPolicy())
        assert not report.passed
        assert report.policy_violations

        # Step 3: NIST compliance
        compliance = NISTComplianceChecker.check(scan, target="app.py")
        from quantum_safe.audit.compliance import ComplianceLevel
        assert compliance.overall_level == ComplianceLevel.NON_COMPLIANT
        assert compliance.non_compliant_controls

        # Step 4: SARIF output is valid JSON
        sarif = json.loads(json.dumps(report.to_sarif()))
        assert sarif["version"] == "2.1.0"

    def test_clean_source_passes_all_checks(self):
        src = "from quantum_safe import HybridKEM, HybridSign\n"
        scan = Scanner.scan_source(src)
        assert not scan.has_blocking_findings

        report = Auditor.audit_source(src)
        assert report.passed

    def test_sbom_pipeline(self):
        sbom = {
            "bomFormat": "CycloneDX",
            "specVersion": "1.4",
            "version": 1,
            "components": [
                {"name": "cryptography", "version": "44.0.5"},
                {"name": "pycryptodome", "version": "3.20.0"},
                {"name": "quantum-safe", "version": "0.1.0"},
            ],
        }
        enriched, assessments = SBOMEnricher.enrich(sbom)
        qs_a = next(a for a in assessments if a.name == "quantum-safe")
        pycrypto_a = next(a for a in assessments if a.name == "pycryptodome")
        assert qs_a.readiness == PQCReadiness.READY
        assert pycrypto_a.readiness == PQCReadiness.NOT_READY
        assert json.dumps(enriched)  # must be JSON-serialisable


