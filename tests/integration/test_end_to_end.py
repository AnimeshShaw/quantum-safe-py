@pytest.mark.requires_liboqs
class TestWithRealBackend:
    def test_hybrid_kem_full_cycle(self):
        kem = HybridKEM()
        kp = kem.generate_keypair()
        hct, ss1 = kem.encapsulate(kp.public)
        ss2 = kem.decapsulate(kp.secret, hct)
        assert ss1 == ss2
        assert ss1.is_hybrid

    def test_hybrid_sign_full_cycle(self):
        signer = HybridSign()
        kp = signer.generate_keypair()
        sm = signer.sign(b"integration test message", kp.secret, context=b"test-v1")
        signer.verify(sm, kp.public)

    def test_envelope_with_real_kem(self):
        kem = HybridKEM()
        kp = kem.generate_keypair()
        sealed = Envelope.seal(b"real payload", kp.public)
        recovered = Envelope.open(sealed, kp.secret)
        assert recovered == b"real payload"

    def test_jwt_with_real_backend(self):
        signer_obj = HybridSign()
        kp = signer_obj.generate_keypair()
        jwt_signer = JWTSigner(kp, issuer="real-integration")
        token = jwt_signer.sign({"sub": "real-user"})
        jwt_verifier = JWTVerifier(kp.public, issuer="real-integration")
        claims = jwt_verifier.verify(token)
        assert claims["sub"] == "real-user"

    def test_scanner_finds_rsa_in_tempfile(self, tmp_path):
        f = tmp_path / "legacy.py"
        f.write_text(
            "from cryptography.hazmat.primitives.asymmetric import rsa\n"
            "key = rsa.generate_private_key(65537, 2048)\n"
        )
        report = Auditor.audit(tmp_path, policy=AuditPolicy())
        assert not report.passed
