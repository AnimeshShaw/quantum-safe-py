# Workflow 3: JWT sign → verify → claim validation
# ---------------------------------------------------------------------------


class TestJWTWorkflow:
    """JWT tokens must round-trip with real Ed25519 verification."""

    def _make_pair(self, issuer="test.example.com"):
        hs = _mock_sign()
        kp = hs.generate_keypair()

        signer = JWTSigner.__new__(JWTSigner)
        signer._keypair = kp
        signer._hedged = True
        signer._issuer = issuer
        signer._algorithm = "Ed25519+ML-DSA-65"
        signer._signer = hs

        verifier = JWTVerifier.__new__(JWTVerifier)
        verifier._public_key = kp.public
        verifier._issuer = issuer
        verifier._audience = None
        verifier._algorithm = "Ed25519+ML-DSA-65"
        verifier._verifier = hs

        return signer, verifier

    def test_sign_and_verify(self):
        signer, verifier = self._make_pair()
        token = signer.sign({"sub": "user123", "role": "admin"})
        claims = verifier.verify(token)
        assert claims["sub"] == "user123"
        assert claims["role"] == "admin"
        assert claims["iss"] == "test.example.com"

    def test_token_has_three_parts(self):
        signer, _ = self._make_pair()
        token = signer.sign({"sub": "u"})
        assert token.count(".") == 2

    def test_tampered_payload_rejected(self):
        import base64
        signer, verifier = self._make_pair()
        token = signer.sign({"sub": "user"})
        header, _, sig = token.split(".")
        fake_payload = base64.urlsafe_b64encode(
            json.dumps({"sub": "admin", "iss": "test.example.com",
                        "iat": 9999999999}).encode()
        ).rstrip(b"=").decode()
        tampered = f"{header}.{fake_payload}.{sig}"
        with pytest.raises(VerificationError):
            verifier.verify(tampered)

    def test_expired_token_rejected(self):
        import time
        signer, verifier = self._make_pair()
        token = signer.sign({"sub": "u", "exp": int(time.time()) - 200})
        with pytest.raises(VerificationError):
            verifier.verify(token)

    def test_zero_expires_in_omits_exp(self):
        signer, verifier = self._make_pair()
        token = signer.sign({"sub": "u"}, expires_in=0)
        payload_b64 = token.split(".")[1]
        padding = "=" * (-len(payload_b64) % 4)
        import base64
        claims_raw = json.loads(base64.urlsafe_b64decode(payload_b64 + padding))
        assert "exp" not in claims_raw


